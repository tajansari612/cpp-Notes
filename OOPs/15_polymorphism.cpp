// Polymorphism is an important concept of object-oriented programming. 
// It simply means more than one form. That is, the same entity (function or 
// operator) behaves differently in different scenarios depending on the context
// they are used 

// For example,
// The + operator in C++ is used to perform two specific functions. 
// When it is used with numbers (integers and floating-point numbers), 
// it performs addition.
// And when we use the + operator with strings, it performs string 
// concatenation.

// We can implement polymorphism in C++ using the following ways:
// Function overloading(method oveloading if inside the class)
// Operator overloading
// constructor overloading
// Function overriding
// Virtual functions


