//Types of Linked List

//01 Singly Linked list :
// It is the commonly used linked list in programs. If we are talking about
// the linked list, it means it is a singly linked list. The singly linked
// list is a data structure that contains two parts, i.e., one is the data
// part, and the other one is the address part, which contains the address
// of the next or the successor node. The address part in a node is also
// known as a pointer.

//02 Doubly linked list
// As the name suggests, the doubly linked list contains two pointers. We
// can define the doubly linked list as a linear data structure with three
// parts: the data part and the other two address part. In other words, a
// doubly linked list is a list that has three parts in a single node,
// includes one data part, a pointer to its previous node, and a pointer to
// the next node.

//03 Circular linked list
// A circular linked list is a variation of a singly linked list. The only
// difference between the singly linked list and a circular linked list is
// that the last node does not point to any node in a singly linked list,
// so its link part contains a NULL value. On the other hand, the circular
// linked list is a list in which the last node connects to the first node,
// so the link part of the last node holds the first node's address. The
// circular linked list has no starting and ending node.

